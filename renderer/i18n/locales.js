const LOCALE_STORAGE_KEY = 'star-locale';
let _localeIpcRenderer;

function _normalizeLocale(locale) {
  return Object.prototype.hasOwnProperty.call(LOCALES, locale) ? locale : 'zh-CN';
}

function _readStoredLocale() {
  try {
    if (typeof localStorage !== 'undefined') {
      return _normalizeLocale(localStorage.getItem(LOCALE_STORAGE_KEY));
    }
  } catch (_) {}
  return 'zh-CN';
}

function _persistLocale(locale) {
  try {
    if (typeof localStorage !== 'undefined') localStorage.setItem(LOCALE_STORAGE_KEY, locale);
  } catch (_) {}
}

function _getLocaleIpcRenderer() {
  if (_localeIpcRenderer !== undefined) return _localeIpcRenderer;
  try {
    _localeIpcRenderer = typeof require === 'function' ? require('electron').ipcRenderer : null;
  } catch (_) {
    _localeIpcRenderer = null;
  }
  return _localeIpcRenderer;
}

function _syncLocaleToMain(locale) {
  try {
    const ipcRenderer = _getLocaleIpcRenderer();
    if (ipcRenderer && typeof ipcRenderer.send === 'function') {
      ipcRenderer.send('os:set-locale', locale);
    }
  } catch (_) {}
}

let currentLocale = _readStoredLocale();

function _formatI18nValue(val, params) {
  if (typeof val !== 'string' || !val) return val;
  if (!params) return val;
  return val.replace(/\{([a-zA-Z0-9_]+)\}/g, (_, k) => {
    const v = params[k];
    return (v === undefined || v === null) ? `{${k}}` : String(v);
  });
}

function t(key, fallback, params) {
  const localeMap = LOCALES[currentLocale] || {};
  const enMap = LOCALES['en'] || {};
  const zhMap = LOCALES['zh-CN'] || {};

  // Fallback order:
  // - Traditional Chinese prefers Simplified then English.
  // - Other locales prefer English then Simplified Chinese.
  // This avoids unexpected Chinese text in ja/ko when a key is missing.
  const raw =
    (localeMap[key] ?? (
      currentLocale === 'zh-TW'
        ? (zhMap[key] ?? enMap[key])
        : (enMap[key] ?? zhMap[key])
    ) ?? fallback ?? key);

  return _formatI18nValue(raw, params);
}

function setLocale(locale) {
  const nextLocale = _normalizeLocale(locale);
  if (LOCALES[nextLocale]) {
    currentLocale = nextLocale;
    _persistLocale(nextLocale);
    _syncLocaleToMain(nextLocale);
    document.documentElement.lang = nextLocale.replace('-', '_');
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const k = el.getAttribute('data-i18n');
      if (k) el.textContent = t(k);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const k = el.getAttribute('data-i18n-placeholder');
      if (k) el.placeholder = t(k);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const k = el.getAttribute('data-i18n-title');
      if (k) el.title = t(k);
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      const k = el.getAttribute('data-i18n-aria-label');
      if (k) el.setAttribute('aria-label', t(k));
    });
    const trayLang = document.getElementById('tray-lang');
    if (trayLang) {
      const names = { 'zh-CN': '简体中文', 'zh-TW': '繁體中文', 'en': 'English', 'ja': '日本語', 'ko': '한국어' };
      trayLang.textContent = names[nextLocale] || nextLocale;
    }
    if (window.onLocaleChange) window.onLocaleChange();
    try {
      if (typeof CustomEvent === 'function') {
        window.dispatchEvent(new CustomEvent('star:locale-change', { detail: { locale: nextLocale } }));
      } else {
        const ev = document.createEvent('CustomEvent');
        ev.initCustomEvent('star:locale-change', false, false, { locale: nextLocale });
        window.dispatchEvent(ev);
      }
    } catch (_) {}
  }
}

